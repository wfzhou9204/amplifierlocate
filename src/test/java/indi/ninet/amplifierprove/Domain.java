package indi.ninet.amplifierprove;

public class Domain {
	private String userid;
	private String ip;
	private int port;
	private int par_num;
	private String pars;
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	public int getPar_num() {
		return par_num;
	}
	public void setPar_num(int par_num) {
		this.par_num = par_num;
	}
	public String getPars() {
		return pars;
	}
	public void setPars(String pars) {
		this.pars = pars;
	}
}
